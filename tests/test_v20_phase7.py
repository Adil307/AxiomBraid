def test_current_release_files_do_not_claim_alpha():
    files = [ROOT / "pyproject.toml", ROOT / "README.md", ROOT / "src/axiombraid/_version.py", ROOT / "src/axiombraid/cli.py", ROOT / "docs/index.md", ROOT / "docs/INSTALLATION.md", ROOT / "docs/PUBLIC_API.md"]
    for path in files:
        text = path.read_text(encoding="utf-8").lower()
        assert "2.0.0a6" not in text
        assert "development status :: 3 - alpha" not in text
